
# 首先，我们需要打印一个消息来欢迎用户
print("欢迎使用这个小程序!")

# 接下来，我们会询问用户的名字
name = input("请问你的名字是什么? ")

# 现在，我们会询问用户的年龄
age = input("你多少岁了? ")

# 将年龄从字符串转换为整数，这样我们就可以进行数学计算了
age = int(age)

# 计算用户达到100岁还需要多少年
years_to_100 = 100 - age


# f"this is a string {age}"
# 告诉用户他们会在多少年后达到100岁
print(f"你好，{name}! 你将在{years_to_100}年后达到100岁。")

print("你好，" + name + "! 你将在" + str(years_to_100) + "年后达到100岁。")
