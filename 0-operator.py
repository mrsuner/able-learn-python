# 让用户输入两个数字
num1 = int(input("请输入第一个数字: "))
num2 = int(input("请输入第二个数字: "))#2

# 使用加法运算符
print(f"{num1} + {num2} = {num1 + num2}")

# 使用减法运算符
print(f"{num1} - {num2} = {num1 - num2}")

# 使用乘法运算符
print(f"{num1} * {num2} = {num1 * num2}")

# 使用除法运算符
print(f"{num1} / {num2} = {num1 / num2}")

# 使用取余运算符
print(f"{num1} % {num2} = {num1 % num2}")

# 使用幂运算符
print(f"{num1} ** {num2} = {num1 ** num2}")

# 使用整除运算符
print(f"{num1} // {num2} = {num1 // num2}")

# 使用比较运算符
print(f"{num1} > {num2} 是 {num1 > num2}")
print(f"{num1} < {num2} 是 {num1 < num2}")
print(f"{num1} == {num2} 是 {num1 == num2}")
print(f"{num1} != {num2} 是 {num1 != num2}")
